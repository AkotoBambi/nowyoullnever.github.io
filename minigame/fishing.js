let frameIndex = 0;
let isWaiting = false;

function showNextFrame() {
  if (frames.length === 0) {
    return;
  }

  asciiFrame.textContent = frames[frameIndex];
  frameIndex = (frameIndex + 1) % frames.length;
}

function pickRandomMessage() {
  if (randomMessages.length === 0) {
    return '...';
  }

  return randomMessages[Math.floor(Math.random() * randomMessages.length)];
}

function resetCatchButton() {
  catchMessage.classList.add('is-hidden');
  catchMessage.textContent = '';
  catchButton.classList.remove('is-hidden');
  isWaiting = false;
}

function castLine() {
  if (isWaiting) {
    return;
  }

  const waitTime = 6000 + Math.random() * 9000;

  isWaiting = true;
  catchButton.classList.add('is-hidden');
  catchMessage.textContent = pickRandomMessage();
  catchMessage.classList.remove('is-hidden');
  window.setTimeout(() => {
    if (isWaiting) {
      catchMessage.textContent = '...';
    }
  }, 5000);
  window.setTimeout(resetCatchButton, waitTime);
}

function startFishing() {
  showNextFrame();
  window.setInterval(showNextFrame, 800);
  catchButton.addEventListener('click', castLine);
}

startFishing();
